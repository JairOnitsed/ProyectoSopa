    void rellenaTable(){
        
        
        String[] columnas = {"Nombre","Puntaje"};
        String[] resultados = new String[2];
        modelo = new DefaultTableModel(null,columnas);
        String query = "";
        
        Conexion cn = new Conexion();
        Connection conex = cn.Conectar();
           
        query = "SELECT * FROM puntajes";
        
        try {
            Statement st = conex.createStatement();
            ResultSet rs = st.executeQuery(query);
            while (rs.next()) {
                resultados[0] = rs.getString("Nombre");
                resultados[1] = rs.getString("Puntaje");
                modelo.addRow(resultados);
            }
            tblPuntajes.setModel(modelo);
        } catch (Exception e) {
            JOptionPane.showMessageDialog(null, e);
        }
    }

